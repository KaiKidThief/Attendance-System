    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(UserName.getText().trim().isEmpty()){
            
            JOptionPane.showMessageDialog(this,"Please Enter The Username");
            UserName.setText("");
            UserName.grabFocus();
            
        }else if(PassWord.getText().trim().isEmpty()){
        
            JOptionPane.showMessageDialog(this,"Please Enter The UserPassword");
            PassWord.setText("");
            PassWord.grabFocus();
        }else{
            
            String Username = UserName.getText();
            String Password = PassWord.getText();
            
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/attendance_system", "root", "");
                PreparedStatement ps = con.prepareStatement("select * from users_pass_info where Username=? and Password=?");
                
                ps.setString(1, Username);
                ps.setString(2, Password);
                
                ResultSet rs = ps.executeQuery();
                
                if(rs.next()){
                    
                    Employee tech = new Employee();
                    this.hide();
                    tech.show();
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Invalid Username and Userpassword");
                    UserName.setText("");
                    PassWord.setText("");
                    UserName.requestFocus();
                }    
            }
            catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(UserLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
                
              
        } 
        
    }//GEN-LAST:event_jButton1ActionPerformed

